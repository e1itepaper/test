package ch02;

public class SubOp02 {

	public static void main(String[] args) {
		int a1 = 5;
		int a2 = 10;
		// int a1 = 5, a2 = 10;
		
		a1 += a2;
		System.out.println("a1 = " + a1 + "\ta2 = " + a2);
		
		a1 -= a2;
		System.out.println("a1 = " + a1 + "\ta2 = " + a2);
		
		a1 *= a2;
		System.out.println("a1 = " + a1 + "\ta2 = " + a2);
		
		a1 /= a2;
		System.out.println("a1 = " + a1 + "\ta2 = " + a2);
		
		a1 %= a2;
		System.out.println("a1 = " + a1 + "\ta2 = " + a2);
		
		
	}

}
