package ch02;

public class BitOp01 {

	public static void main(String[] args) {
		int i1 = 10, i2 = 6;
		System.out.println("i1 & i2 = "+(i1 & i2));

		System.out.println("i1 | i2 = " +(i1 | i2)); 

		System.out.println("i1 ^ i2 = "+(i1 ^ i2));

		System.out.println("~i1 = "+ (~i1));
		
	}
}
