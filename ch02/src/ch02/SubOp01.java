package ch02;

public class SubOp01 {

	public static void main(String[] args) {
		int a1 = 4;
		a1 += 4; // a1 = a1 + 4;
		System.out.println("a1 = " + a1);
		a1 -= 4; // a1 = a1 - 4;
		System.out.println("a1 = " + a1);
		a1 *= 4; // a1 = a1 * 4;
		System.out.println("a1 = " + a1);
		a1 /= 4; // a1 = a1 / 4;
		System.out.println("a1 = " + a1);
		a1 %= 4; // a1 = a1 % 4;
		System.out.println("a1 = " + a1);
	}

}
